package sec1;

public class Var03 {
	public static void main(String[] args) {
		//primitive type(기본 자료형) : 하나의 데이터만 저장가능한 구조
		boolean a=true;
	//정수	
		byte b=20, c=127;	//-128~127 -> 1byte(8bit = 2의8승 =256가지)
		short d=6450;		//1short = 2byte (16bit) 
		int e=65000;		//1int = 4byte   (32bit)
		long f=650000;		//1long = 8byte  (64bit)
	//실수
		float g=3.14f;		//1float = 4byte (32bit)
		double h=3.14;		//1double = 8byte(64bit)
	//문자 / 문자열
		char i ='k';		//1char = 2byte  (16bit)
	    String j = "kim";	//
	    
		
	}

}
